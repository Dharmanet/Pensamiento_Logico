# Propiedades algebraicas básicas

# Propiedad conmutativa de la suma
a = 5
b = 7
print("a =", a, "b =", b)
print("a + b =", a + b)
print("b + a =", b + a)

# Propiedad conmutativa de la multiplicación
c = 3
d = 8
print("c =", c, "d =", d)
print("c * d =", c * d)
print("d * c =", d * c)

# Propiedad asociativa de la suma
e = 3
f = 4
g = 5
print("e =", e, "f =", f, "g =", g)
print("(e + f) + g =", (e + f) + g)
print("e + (f + g) =", e + (f + g))

# Propiedad asociativa de la multiplicación
h = 2
i = 3
j = 4
print("h =", h, "i =", i, "j =", j)
print("(h * i) * j =", (h * i) * j)
print("h * (i * j) =", h * (i * j))

# Propiedad distributiva de la suma y la multiplicación
k = 2
l = 3
m = 4
print("k =", k, "l =", l, "m =", m)
print("k * (l + m) =", k * (l + m))
print("k * l + k * m =", k * l + k * m)

n = 5
o = 6
p = 7
print("n =", n, "o =", o, "p =", p)
print("n + o * p =", n + o * p)
print("(n + o) * (n + p) =", (n + o) * (n + p))

# Propiedad de identidad de la suma
q = 5
print("q =", q)
print("q + 0 =", q + 0)
print("0 + q =", 0 + q)

# Propiedad de identidad de la multiplicación
r = 6
print("r =", r)
print("r * 1 =", r * 1)
print("1 * r =", 1 * r)
